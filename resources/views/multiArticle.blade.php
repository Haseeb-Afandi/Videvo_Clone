        <h1 class="title">License Types and Usage: Full Guide</h1>
        
        <p> Welcome to our guide on licensing and the usage of clips on Videvo.net. Below you will find all the information you need to ensure you are using clips from Videvo correctly, and not breaking any rules in the process. Firstly, it is important to point out that we license clips on Videvo in a number of different ways, and there are some clips on Videvo that you may not be able to use for certain types of project. Furthermore, you may have to credit the author of the clip. In this article we will cover the different types of license, usage, and how to credit the author if it is required.</p>
        <h3>Where can I find license and author information for a clip?</h3>
        <p> License, Usage and Author information for each clip is displayed on the clip download page in the top section to the right-hand side of the video player. Please see the image below for reference:</p>
        <p> <img src="//d33v4339jhl8k0.cloudfront.net/docs/assets/5d1c717d2c7d3a5cd38e7245/images/5e39b87904286364bc94d98f/file-rmBvFETsio.jpg" style="width: 547px;"></p>
        <h3>The difference between licence and usage</h3>
        <p> It is important to note that on Videvo we differentiate between license and usage. Broadly speaking, the license defines what permissions you have in terms of editing the clip, whether you need to provide a credit, where you can publish the clip and so on. Usage dictates whether or not you may use the clip for commercial purposes to promote a product, brand, or service. If the usage states “All projects and media” you may use the clip for commercial purposes. If it states “Editorial Use Only”, you may not use the clip for commercial purposes. You can find information on the difference between <a href="{{ url('/')}}//blog/editorial-usage-a-complete-guide-to-using-footage-marked-editorial-use-only/">Commercial and Editorial content here</a></p>
        <h3>Royalty-Free License</h3>
        <p> This is our most common license and applies to both free and premium clips. It means that once you download the clip, you may use it&nbsp;in as many projects as you like, forever. It's yours to keep, but this does not mean you "own" the clip; the copyright remains with the creator. There are no limitations on where you can publish your work (e.g. online, broadcast etc.), but be aware that if it has been marked as Editorial Use Only, you can not use it in a commercial project (to promote a product or service). See our guidelines on "Editorial Use Only" content.</p>
        <p> Royalty-Free License overview:</p>
        <ul>
        <li>Download once and use in as many projects as you like</li>
        <li>You can edit the clip(s) in any way you like</li>
        <li>You are not required to credit the author of the clip(s)</li>
        <li>You may use and publish the clip(s) worldwide and on any platform, including web, broadcast, shows, theatre, apps, and games</li>
        <li>You may NOT redistribute the clip(s) in their original form (e.g. making the clip available as a stock clip for download on another website)</li>
        </ul>
        <h3>Videvo Attribution License</h3>
        <p> This license allows for the use of the clip free of charge for perpetuity, but you must provide a credit to the original author of the clip. For more information about how to credit, scroll down this article to the section “How to provide a credit”. Similar to the Royalty Free License, you may not redistribute the clip(s) in their original form on other platforms or services, for example, torrent websites or other stock agencies.</p>
        <ul>
        <li>You may use the clip(s) for free and for perpetuity</li>
        <li>You must credit the author of the clip(s) in your production</li>
        <li>You may use and publish the clip(s) worldwide and on any platform, including web, broadcast, shows, theatre, apps, and games</li>
        <li>You may not redistribute the clip(s) in their original form (download and then re-upload the clip(s) elsewhere, e.g. torrent websites)</li>
        </ul>
        <h3>Creative Commons 3.0 Unported (CC-BY)</h3>
        <p> This is similar to the Videvo Attribution License, but you are free to distribute the clip elsewhere in its original, unedited form. You must attribute the original author in all productions as well as any distribution channels in which the clip been made available for download. This license also allows for commercial use, although special attention should be paid to ensure that no identifiable persons, brands or private property appear&nbsp;in the clip(s)&nbsp;unless a model/property release has first been obtained.</p>
        <ul>
        <li>You may use the clip(s) for free and for perpetuity</li>
        <li>You must credit the author of the clip(s) in your production</li>
        <li>You may use and publish the clip(s) worldwide and on any platform, including web, broadcast, shows, theatre, apps, and games</li>
        <li>You may share, adapt, and redistribute the clip(s), but you may not sell the clip(s)</li>
        </ul>
        <p> This is a rough overview of the CC 3.0 guidelines. For full information, read the <a href="https://creativecommons.org/licenses/by/3.0/">CC 3.0 Guidelines here</a></p>
        <h3>NASA License</h3>
        <p> <a href="https://creativecommons.org/licenses/by/3.0/"></a>Please refer to the guidelines for use of NASA imagery, <a href="{{ url('/')}}//wp-content/uploads/2012/07/NASA-License-Agreement.pdf">located here</a></p>
        <h3>Public Domain License</h3>
        <p> The ‘Public Domain’ License covers clips hosted on the site that are in the public domain. Public domain clips by the United States Fish and Wildlife Service may be used “as long as there is no implied U.S. Government / U.S. Department of the Interior / Fish and Wildlife Service endorsement, promotion, or support of a particular organization, product, or position.”</p>
        <p> For these clips, please give credit to:&nbsp;U.S. Fish &amp; Wildlife Service, National Conservation Training Center, Creative Imagery.</p>
        <p> <a href="{{ url('/')}}//wp-content/uploads/2012/07/NASA-License-Agreement.pdf"></a>Read more about the <a href="https://fairuse.stanford.edu/overview/public-domain/welcome/">public domain here</a>.</p>
        <hr>
        <h3>Usage: Commercial Use or Editorial Use Only</h3>
        <p> As we mentioned above, “Usage” defines whether a clip can be used for commercial purposes (all projects and media), or for editorial projects only (Editorial Use Only). On the clip download page, under “Usage” you will see one of two variations: “All projects and media” or “Editorial Use Only”. The screenshots below illustrate this:</p>
        <p> <img src="//d33v4339jhl8k0.cloudfront.net/docs/assets/5d1c717d2c7d3a5cd38e7245/images/5e39b9c62c7d3a7e9ae73984/file-6xazzOUzzO.jpg" alt="" style="display: block; margin: auto;"></p>
        <div>
        <p style="text-align: center;"> Scenario 1: “All projects and media”. You may use the clip for commercial purposes. </p>
        <p style="text-align: center;"> </p>
        <p style="text-align: center;"> <img src="//d33v4339jhl8k0.cloudfront.net/docs/assets/5d1c717d2c7d3a5cd38e7245/images/5e39b9fb2c7d3a7e9ae73987/file-iGQiXLEJ4g.jpg" alt="" style="display: block; margin: auto;"> </p>
        </div>
        <div style="text-align: center;">
        <p style="text-align: center;"> Scenario 2: “Editorial Use Only”. You may not use the clip commercially. </p>
        <p style="text-align: center;"> </p>
        </div>
        <p> Why do we make this distinction? Clips are normally marked as Editorial Use Only when they contain identifiable persons, private property that is the main focus of the shot, brands or logos that are the main focus of the shot, or artwork or copyrighted works. Without a release form from the person(s) appearing in the shot or the owner of the property, it is not possible to use the clip for commercial purposes. However, you may use the clip for editorial purposes.</p>
        <h3>What does “Editorial” mean and what projects are classed as Editorial?</h3>
        <p> Editorial content is defined as content that is newsworthy or documentary-worthy, that is providing a commentary on a story outside of a promotional context. For example, all news programming is considered editorial, because it is providing a commentary on real-world events as they happen. Documentaries are considered editorial, providing that the aim of the documentary is not to promote a brand product or service. Even if you are selling your documentary, it is still classed as editorial.&nbsp;Editorial images can also be used for educational purposes, providing the educational content is presented as a learning aid and is factual.</p>
        <p> Read our <a href="{{ url('/')}}//blog/editorial-usage-a-complete-guide-to-using-footage-marked-editorial-use-only/">full guide on editorial use here</a>.</p>
        <p> Here are some useful guides on the topic of editorial images/videos and intellectual property:</p>
        <ul>
        <li><a href="https://nimia.com/documentary-vs-commercial-requirements-for-legal-releases" target="_blank">The Nimia Guide on Editorial Documentaries&nbsp;</a></li>
        <li><a href="https://www.shutterstock.com/blog/2010/05/editorial-vs-commercial-images/" target="_blank">Shutterstock’s Guide to Editorial Images</a></li>
        <li><a href="http://wiki.gettyimages.com/" target="_blank">Getty’s Intellectual Property Wiki Page</a></li>
        </ul>
        <h3>Does cancelling your subscription effect your license?</h3>
        <div>
        &nbsp;Any clips that you download during your subscription are still subject to the same license conditions after you cancel. So you can continue to use the content under the same conditions and there is no need to stay subscribed.&nbsp;
        </div>
        <p> Your premium subscription just gives you access to download that premium content in the first place.</p>
