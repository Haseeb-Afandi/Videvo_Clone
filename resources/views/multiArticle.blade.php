        <h1 class="title">How do I provide attribution?</h1>
        
        <p> Many clips on Videvo require that you provide a credit to the author (also referred to as <em>attribution</em>). These are clips that have been licensed under the Videvo Attribution License or Creative Commons 3.0 Unported (CC-BY). License and author information are viewable on the download page.&nbsp;</p>
        <h3>The format</h3>
        <p> You should credit the name of the author exactly as it appears on the download page, followed by <a href="http://www.videvo.net">www.videvo.net</a>.&nbsp;</p>
        <section class="callout dashed">
        <p> For example if the author name is "Beachfront", the credit should read:&nbsp; <strong>"Stock footage provided by Beachfront, downloaded from </strong><a href="http://www.videvo.net" "=""><strong>www.videvo.net"</strong></a></p>
        </section>
        <p> For broadcast, the end credits section for stock content is often referred to as Archive, with each provider/artist on a separate line. In this instance, the credit should go on a single line in the following format "Videvo.net: &lt;author name&gt;". In the above example this would read "Videvo.net: Beachfront". If the author is Videvo, the credit should simply read "Videvo".</p>
        <h3>Where to display the credit?</h3>
        <p> The credit can be displayed in the end credits of your production, and/or a text description published alongside your video (for example, the YouTube video description) with an active hyperlink for <a href="http://www.videvo.net">www.videvo.net</a>.</p>
        <h3>Crediting the author for Creative Commons 3.0 Clips</h3>
        <p> Creative Commons outline their own guidelines on how to credit the author of media licensed under CC 3.0 (CC-BY). You can read the <a href="https://wiki.creativecommons.org/wiki/License_Versions#Detailed_attribution_comparison_chart" target="_blank">guidelines here</a></p>
