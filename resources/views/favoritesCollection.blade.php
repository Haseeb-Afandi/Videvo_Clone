<h1 class="title">Favoriting &amp; Saving Clips to Your Collection</h1>

<h1> Favoriting &amp; Saving&nbsp;Clips to Your Collection</h1>
<p> On Videvo we offer several ways for users to collate content. The first is the Favorites function.</p>
<p> <img src="//d33v4339jhl8k0.cloudfront.net/docs/assets/5d1c717d2c7d3a5cd38e7245/images/62208ae2c1688a6d26a7162a/file-sGit9JjjlT.png" style="width: 481px;"></p>
<p> By clicking on the Favorite icon on any media on the site, you will find that these items will all be collated into your accounts personal favorites list found here while logged in: <a href="{{ url('/')}}/favorites/">{{ url('/')}}/my-favorites/</a></p>
<p> <img src="//d33v4339jhl8k0.cloudfront.net/docs/assets/5d1c717d2c7d3a5cd38e7245/images/62208a6f2ce7ed0fb0911777/file-2HAklvcnN7.png"></p>
<p> If you wish to organise your content into particular groupings we also offer Collections functionality. While hovering over any media on the site, click on the plus icon to bring up the collections menu.&nbsp;</p>
<p> <img src="//d33v4339jhl8k0.cloudfront.net/docs/assets/5d1c717d2c7d3a5cd38e7245/images/62208b27c1688a6d26a7162d/file-q2ne8Z87ki.png" style="width: 522px;"></p>
<p> Here you can add an item to any number of already created collections, or create a brand new collection from scratch. You can find all collections while logged in from here: <a href="{{ url('/')}}/collections/">{{ url('/')}}/my-collections/</a> Within this area you can amend and rename existing collections, create new collections or delete collections that are no longer of use.</p>
<p> <img src="//d33v4339jhl8k0.cloudfront.net/docs/assets/5d1c717d2c7d3a5cd38e7245/images/62208c062ce7ed0fb0911789/file-YPVJss40RF.png" style="width: 715px;"></p>
<p> Items added to collections will show a blue tick icon to give you better visibility on what has been used previously. If you wish to move an item to another collection then click on th blue tick, then the check mark of the collections you would like the item to also live in. You can also remove the item from the collection entirely by simply clicking on the blue tick icon and unticking from the collection it is allocated to.</p>
<p> <img src="//d33v4339jhl8k0.cloudfront.net/docs/assets/5d1c717d2c7d3a5cd38e7245/images/62208b6ec1688a6d26a71630/file-ECpDOC1xmo.png"></p>
<p> You can also find both your Favorites and Collections from the user dropdown menu.</p>
<p> <img src="//d33v4339jhl8k0.cloudfront.net/docs/assets/5d1c717d2c7d3a5cd38e7245/images/62208bb1ab585b230a89cb5d/file-PcQASL5TIP.png"></p>
